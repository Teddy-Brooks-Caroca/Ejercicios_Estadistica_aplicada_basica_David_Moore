# El tratamiento más común para combatir el cáncer de mama consistía en la 
# extirpación completa del pecho. Hoy en día, se suele extirpar únicamente el 
# tumor y los ganglios linfáticos circundantes, aplicando después radioterapia 
# en la zona afectada. El cambio de tratamiento se produjo tras un amplio 
# experimento médico que comparó ambas técnicas: se seleccionó al azar dos 
# grupos de enfermas; cada uno siguió un tratamiento distinto y se realizó
# un minucioso seguimiento de las pacientes para comprobar el periodo de 
# supervivencia.

# (a) ¿Cuál es la variable explicativa y cuál es la variable respuesta?

"
Variable explicativa: Tratamiento
Variable de respuesta: El período de supervivencia
"
# (b) ¿Son variables categóricas o cuantitativas?

"
Categórica: Tratamiento
Cunatitativa: El período de supervivencia
