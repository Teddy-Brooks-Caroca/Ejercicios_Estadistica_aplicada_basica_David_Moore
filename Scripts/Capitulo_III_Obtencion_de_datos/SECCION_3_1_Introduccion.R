# PREGUNTA 3.2 - Enseñar a leer
# **************************************************

# Una educadora quiere comparar la efectividad de un programa informático 
# que enseña a leer con el método estándar. La educadora valora la capacidad 
# lectora de cada uno de los niños de un grupo de segundo curso de enseñanza 
# general básica. A continuación la educadora divide al azar al grupo en dos 
# subgrupos. Un subgrupo utiliza habitualmente el ordenador, mientras que el 
# otro utiliza el método estándar. Al final del curso, la educadora vuelve a 
# valorar la capacidad lectora de los niños y compara los incrementos producidos.

# (a) Este estudio, ¿es un experimento? Justifica tu respuesta.

"Sí, es un experimento, porque la educadora asigna aleatoriamente a los niños a dos 
métodos de enseñanza, manipulando deliberadamente la variable explicativa para observar 
su efecto."

# (b) ¿Cuál es la variable respuesta?

"La variable respuesta es el incremento en la capacidad lectora al final del curso."

# (c) ¿Cuál es la variable explicativa?

"En este caso, el método de enseñanza de lectura de lectura (programa informático vs. método estándar). "

