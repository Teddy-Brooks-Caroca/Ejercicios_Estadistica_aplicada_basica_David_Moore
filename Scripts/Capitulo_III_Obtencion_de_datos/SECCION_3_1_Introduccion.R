# PREGUNTA 3.3 - Efectos de la propaganda
# **************************************************

# En 1940, un psicólogo llevó a cabo un experimento para estudiar el efecto de 
# la propaganda sobre la actitud de un grupo de personas hacia un Gobierno 
# extranjero. El psicólogo preparó una prueba para determinar la actitud de la 
# gente hacia el Gobierno alemán y la hizo pasar a un grupo de estudiantes. 
# Después de hacerles leer propaganda alemana durante algunos meses, volvió a 
# pasar la prueba a los estudiantes para ver si habían cambiado su actitud.

# Desgraciadamente, mientras se estaba desarrollando la prueba, Alemania 
# invadió Francia.

# Explica de forma clara por qué el efecto de la confusión impide determinar 
# el efecto de la lectura de propaganda.

"El estudio queda confundido porque entre la primera y segunda medición ocurrió un hecho histórico
relevante (la invasión de Francia por Alemania). Este evento externo pudo influir fuertemente en la 
actitud de los estudiantes hacia Alemania, de manera independiente a la propaganda. Por tanto, no es 
posible distinguir si el cambio en la actitud se debe a la propaganda o a la invasión: ambas variables 
están mezcladas (confusión)."

