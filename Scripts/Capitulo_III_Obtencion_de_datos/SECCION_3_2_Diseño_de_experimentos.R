# PREGUNTA 3.33 - Sellado de envases
# **************************************************

# Un fabricante de productos alimenticios utiliza envases que se sellan en la 
# parte superior mediante la aplicación de una mordaza caliente después del 
# llenado. El cliente separa las piezas selladas para poder abrir el envase.

# ¿Qué efecto tiene la temperatura de la mordaza sobre la fuerza necesaria
# para poder abrir posteriormente el envase? Para responder a esta pregunta, unos
# ingenieros experimentan con 20 pares de envases. Los ingenieros sellan 5 pares a
# cada una de las siguientes temperaturas: 115 °C, 130 °C, 150 °C y 170 °C. Luego,
# determinan la fuerza necesaria para separar cada precinto.

# (a) ¿Cuáles son las unidades experimentales?

"Las unidades experimentales son los envases (o, más precisamente, cada par de envases) 
a los que se aplica el sellado."

# (b) Existe un factor (variable explicativa). ¿Cuál es y cuáles son sus niveles?

"El factor es la temperatura de la mordaza.
Sus niveles son 115 °C, 130 °C, 150 °C y 170 °C."

# (c) ¿Cuál es la variable respuesta?

"La variable respuesta es la fuerza necesaria para separar el precinto del envase."

