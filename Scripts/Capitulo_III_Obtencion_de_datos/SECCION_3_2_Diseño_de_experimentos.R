# PREGUNTA 3.32 - Drepanocitemia
# **************************************************

# La drepanocitemia es una enfermedad de los glóbulos rojos que en EEUU 
# afecta principalmente a los negros. Esta enfermedad puede causar mucho 
# dolor y complicaciones. La hydroxyurea, ¿puede reducir el dolor causado
# por esta enfermedad?

# Un estudio del National Institute of Health de EE UU proporcionó la 
# hydroxyurea a 150 enfermos y un placebo (falsa medicación) a otros 150.
# Posteriormente, los investigadores hicieron un recuento de los episodios de dolor
# sufridos por cada enfermo.

# (a) ¿Cuáles son las unidades experimentales o sujetos?

"Las unidades experimentales son los 300 pacientes con drepanocitemia que participaron en el estudio."

# (b) Identifica el factor.

"El factor es el tipo de tratamiento administrado"

# (c) Identifica los tratamientos.

"Los tratamientos son: a la mitad se le proporcionó el medicamento (hidroxyurea),
a la otra itad un placebo"

# (d) Identifica la variable respuesta.

"La variable respuesta es el número de episodios de dolor sufridos por cada paciente durante el 
período del estudio."

